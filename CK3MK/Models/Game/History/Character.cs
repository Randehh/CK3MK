namespace CK3MK.Models.Game.History {
	public class Character : BaseGameModel {
		public GameModelAttributeString Id { get; set; } = new GameModelAttributeString();
		public GameModelAttributeString Name { get; set; } = new GameModelAttributeString(true);
		public GameModelAttributeString Dna { get; set; } = new GameModelAttributeString();
		public GameModelAttributeBool Female { get; set; } = new GameModelAttributeBool();
		public GameModelAttributeInt Martial { get; set; } = new GameModelAttributeInt();
		public GameModelAttributeInt Prowess { get; set; } = new GameModelAttributeInt();
		public GameModelAttributeInt Diplomacy { get; set; } = new GameModelAttributeInt();
		public GameModelAttributeInt Intrigue { get; set; } = new GameModelAttributeInt();
		public GameModelAttributeInt Stewardship { get; set; } = new GameModelAttributeInt();
		public GameModelAttributeInt Learning { get; set; } = new GameModelAttributeInt();
		public GameModelAttribute<List<string>> Trait { get; set; } = new GameModelAttribute<List<string>>();
		public GameModelAttributeString Father { get; set; } = new GameModelAttributeString();
		public GameModelAttributeString Mother { get; set; } = new GameModelAttributeString();
		public GameModelAttributeBool DisallowRandomTraits { get; set; } = new GameModelAttributeBool();
		public GameModelAttributeString Religion { get; set; } = new GameModelAttributeString(true);
		public GameModelAttributeString Culture { get; set; } = new GameModelAttributeString(true);
		public GameModelAttributeString Dynasty { get; set; } = new GameModelAttributeString();
		public GameModelAttributeString DynastyHouse { get; set; } = new GameModelAttributeString();
		public GameModelAttributeString GiveNickname { get; set; } = new GameModelAttributeString();
		public GameModelAttributeString Sexuality { get; set; } = new GameModelAttributeString();
		public GameModelAttributeInt Health { get; set; } = new GameModelAttributeInt();
		public GameModelAttributeInt Fertility { get; set; } = new GameModelAttributeInt();
	}
}
